export function getMunicipalitiesByStateId(stateId: number): Municipality[] {
	const base = dzMunicipalitiesMap[stateId] || [];
	const fromCsv = dzMunicipalitiesMapCsv[stateId] || [];
	if (base.length === 0 && fromCsv.length === 0) return [];
	// Merge unique by municipality_name, prefer Arabic from CSV when available
	const seen = new Set<string>();
	const merged: Municipality[] = [];
	for (const m of [...fromCsv, ...base]) {
		const key = m.municipality_name.trim();
		if (seen.has(key)) continue;
		merged.push({ id: m.id, municipality_name: m.municipality_name });
		seen.add(key);
	}
	return merged.sort((a, b) => a.municipality_name.localeCompare(b.municipality_name, 'ar'));
}


